/**
 * Aquí se rupera el usuario seleccionado de Sugerencias y busca su correspondencia en el grafo.
 * Si encuentra una correspondencia establece el texto de SeguirA en el usuario seleccionado.
 *
 * @param evt  Este parámetro no se utiliza en el método.
 */

    private void SugerenciasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_SugerenciasItemStateChanged
        // TODO add your handling code here:
        String Uselected =(String) Sugerencias.getSelectedItem();
        for (int i = 0; i < Ventana1.grafo.getNumVerts(); i++) {
            if (Uselected == Ventana1.grafo.getTabAdy()[i].getUsuario()){
                SeguirA.setText(Uselected);
            }
        }
    }//GEN-LAST:event_SugerenciasItemStateChanged
/**
 * Se recupera el texto de los campos UsuarioNuevo y SeguirA, y crea un nuevo arco en el grafo entre estos dos usuarios.
 * Luego borra el texto del campo SeguirA y muestra un mensaje indicando que ahora sigue al usuario seleccionado.
 *
 * @param evt El evento de acción. Este parámetro no se utiliza en el método
 */

    private void SEGUIRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEGUIRActionPerformed
        // TODO add your handling code here:
        String UsuarioInput = "";
        UsuarioInput= UsuarioNuevo.getText();
        String SeguirA_ = SeguirA.getText();
        boolean bool =Ventana1.grafo.nuevoArco(UsuarioInput,SeguirA_ );
       
        if (bool){
            JOptionPane.showMessageDialog(null, "A partir de ahora sigue a:  "+SeguirA_  + "\nSi desea seguir a alguien más, seleccionelo en la lista!");
            
        }
       SeguirA.setText("");
       
    }//GEN-LAST:event_SEGUIRActionPerformed

