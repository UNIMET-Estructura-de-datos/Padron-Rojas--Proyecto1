/**
 * Este método Recupera el texto del campo UsuarioNuevo y lo valida.
 * Si el nombre de usuario es válido, crea un nuevo usuario en el grafo
 * @param evt El evento de acción. Este parámetro no se utiliza en el método.
 */

    private void REGISTROActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_REGISTROActionPerformed
          
          String UsuarioInput = "";
          UsuarioInput= UsuarioNuevo.getText();
          //UsuarioNuevo.setText("");
        if (Funciones.Validar(UsuarioInput)){
              boolean bool =Ventana1.grafo.NewUsuario(UsuarioInput);
              if (bool){
                  UsuarioNuevo.setEditable(false);
                  JOptionPane.showMessageDialog(null, "Usuario registrado con exito!");
                  }else{
              UsuarioNuevo.setText("");
              UsuarioNuevo.setEditable(true);
              }
              
              
        }
        
      
        
        
        
    }//GEN-LAST:event_REGISTROActionPerformed

